int	ft_strcmp(char *s1, char *s2)
{
	int	test;

	test = 0;
	while (s1[test] == s2[test] && (s1[test] != '\0' || s2[test] != '\0'))
	{
		test++;
	}
	return (s1[test] - s2[test]);
}
